USE DR_DB;

CREATE TABLE TBEMPRESA (
    CDEMPRESA INTEGER PRIMARY KEY NOT NULL AUTO_INCREMENT,
    NOME VARCHAR(45)
);

CREATE TABLE TBUSUARIO (
    CDUSUARIO INTEGER PRIMARY KEY NOT NULL,
    LOGIN VARCHAR(45),
    SENHA VARCHAR(45),
    NOME VARCHAR(45),
    CDPERMISSAO INTEGER NOT NULL	
);


CREATE TABLE TBOM (
    CDOM INTEGER PRIMARY KEY NOT NULL,
    TPOM VARCHAR(45) NOT NULL,
    DTGERACAO DATETIME,
    DTBAIXA_MANUT DATETIME,
    DTBAIXA_SETOR DATETIME,
    DTBAIXA_ADMIN DATETIME,
    OBS VARCHAR(500),
    PRIORIDADE INTEGER  NOT NULL
);



